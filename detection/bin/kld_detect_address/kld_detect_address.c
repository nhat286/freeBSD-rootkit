// function that is called when the module is loaded and unloaded.
static int
load(struct module *module, int cmd, void *arg) {

    switch (cmd) {
    case MOD_LOAD: {

        for (unsigned int i = 0; i < SYS_MAXSYSCALL; i++) {
            if (sysent[i].sy_call != sy_call_addresses[i]) {
                return (1);
            }
        }

        break;
    }

    default:
        break;
    }

    return (0);
}

